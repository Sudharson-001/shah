package com.example.excel;

public class EmployeeDetails {

	public int empId;
	public String empName;
	public String empMail;
	public int empAge;
	
	public EmployeeDetails(int empId, String empName, String empMail, int empAge) {
		
		this.empId = empId;
		this.empName = empName;
		this.empMail = empMail;
		this.empAge = empAge;
	}
	

}


